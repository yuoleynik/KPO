
// Класс пар координат двух точек.
public class PairsOfCoordinates {

    public int x1;
    public int y1;

    public int x2;
    public int y2;

    public PairsOfCoordinates(int first, int second, int third, int fourth) {
        x1 = first;
        y1 = second;
        x2 = third;
        y2 = fourth;
    }
}